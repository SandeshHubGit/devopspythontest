# dictionary --> unordered, key-value pair,

mydic = {
    "name": "sandesh",
    "age": 29,
    "bmi": 20.5
} 
print(mydic["age"])

# update the value of key
mydic = {
    "name": "sandesh",
    "age": 29,
    "bmi": 20.5
} 
mydic["age"] = 31
print(mydic["age"])


# for loop 
for i in mydic:
    print(i, mydic[i])

